1、获取DHA补充剂主要有两种途径：直接获取和间接获取。

    i、直接来源：从藻类提取的藻油和从鱼类脂肪中提取的鱼油。

        i、 安全因素：藻油和鱼油其实都很安全，鱼油含有EPA会降血脂，使血管扩张，容易引起孕妇大出血，出于这个考虑，选择藻油更适合孕产妈妈，
                     如果一定要选择鱼油DHA，应选择含 DHA量大于EPA的量的鱼油。

        ii、污染问题：藻油DHA是直接从海洋单细胞藻类提取，通过发酵提炼出的DHA，完全不介入海洋环境，不经过食物链传递，
                    因此不含任何色素，不含重金属等污染。

                    鱼油DHA是先深海鱼通过食入藻类，再从鱼类的脂肪中提取出的，需要经过食物链的传递，存在成长过程富集的汞等重金属污染。

        iii、结论：藻油更合适孕妇。

    ii、间接来源：如核桃油、亚麻籽油里的α-亚麻酸可转化成DHA，转化率不高，但也能补充部分DHA。


3、结论：

   i、乐辅智DHA软胶囊原料品种选自马泰克深海微藻T油，DHA纯度更高>40%(其它藻类DHA含量>35%)，稳定性更好，抗氧化能力更强，几乎不含EPA，
      相比其它藻类来源的DHA产品更优越。

   ii、鱼油DHA中的DHA：EPA比例为一般在4：1或5：1，DHA的吸收率在20%左右，这对于需要补充DHA的孕妇来说量是远远不够的。
        同时EPA会稀释血液浓度，加速血液循环，降低血液的营养运载能力，此外孕妇服用的EPA也会被胎儿吸收可能会出现胎儿不合理运动而引发的早产现象。



